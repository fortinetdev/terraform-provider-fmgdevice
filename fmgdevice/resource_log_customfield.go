func resourceLogCustomFieldCreate(d *schema.ResourceData, m interface{}) error {
	c := m.(*FortiClient).Client
	c.Retries = 1

	paradict := make(map[string]string)
	wsParams := make(map[string]string)
	cfg := m.(*FortiClient).Cfg
	adomv, err := adomChecking(cfg, d)
	if err != nil {
		return fmt.Errorf("Error adom configuration: %v", err)
	}

	device_name, err := getVariable(cfg, d, "device_name")
	if err != nil {
		return err
	}
	device_vdom, err := getVariable(cfg, d, "device_vdom")
	if err != nil {
		return err
	}
	paradict["device"] = device_name
	paradict["vdom"] = device_vdom

	obj, err := getObjectLogCustomField(d)
	if err != nil {
		return fmt.Errorf("Error creating LogCustomField resource while getting object: %v", err)
	}
	wsParams["adom"] = adomv

	update_if_exist := getUpdateIfExist(c, d)
	mkey_tf, mkey_ok := d.GetOk("fosid")
	mkey := fmt.Sprint(mkey_tf)
	o := make(map[string]interface{})
	existing := false

	if update_if_exist && mkey_ok {
		// check existing
		o, err = c.ReadLogCustomField(mkey, paradict)
		if err == nil && o != nil {
			existing = true
			// update if existing
			o, err = c.UpdateLogCustomField(obj, mkey, paradict, wsParams)
			if err != nil {
				return fmt.Errorf("Error updating LogCustomField resource: %v", err)
			}
		}
	}

	if !existing {
		_, err = c.CreateLogCustomField(obj, paradict, wsParams)
		if err != nil {
			return fmt.Errorf("Error creating LogCustomField resource: %v", err)
		}

	}

	d.SetId(getStringKey(d, "fosid"))

	return resourceLogCustomFieldRead(d, m)
}

